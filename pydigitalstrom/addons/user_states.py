def serialize_user_state_for_save(state: UserState) -> dict[str, Any]:
    """Build the save-payload in the format the dSS web UI uses.

    Verified via DevTools capture 2026-05-30 (custom-state create). The dSS
    expects a unified payload with a ``type`` discriminator field (matching
    the category) - all other category-specific containers must be present
    too, mostly as null/empty defaults.

    Captured custom-state save body (decoded)::

        {
          "name": "test", "showOnPhone": null, "type": "custom-states",
          "setName": "1", "resetName": "2", "id": null,
          "activityState": {},
          "combinedState": {"and": []},
          "sensorState": {},
          "consumptionState": {
            "dSID": null, "setRetriggeringLag": 0, "resetRetriggeringLag": 0
          },
          "powerState": null,
          "productionP1State": null,
          "consumptionP1State": null,
          "productionModbusState": null,
          "consumptionModbusState": null,
          "apartmentPowerState": null,
          "windowState": []
        }

    For combined-states and triggered-states the relevant container fields
    (combinedState / activityState) are populated from the typed model
    fields; the payload still includes all the other null defaults.
    """
    payload: dict[str, Any] = {
        "name": state.name,
        "showOnPhone": state.show_on_phone,
        "type": state.category.value,
        "setName": state.set_name,
        "resetName": state.reset_name,
        "id": state.state_id or None,
        # Universal container defaults - the dSS REQUIRES these to be present
        "activityState": {},
        "combinedState": {"and": []},
        "sensorState": {},
        "consumptionState": {
            "dSID": None,
            "setRetriggeringLag": 0,
            "resetRetriggeringLag": 0,
        },
        "powerState": None,
        "productionP1State": None,
        "consumptionP1State": None,
        "productionModbusState": None,
        "consumptionModbusState": None,
        "apartmentPowerState": None,
        "windowState": [],
    }

    # Category-specific overrides
    if state.category is UserStateCategory.COMBINED:
        # combinedState.and = [{state, value, addonid}, ...]
        # Note: dSS uses ``addonid`` (no hyphen) in this specific container,
        # inconsistent with ``addon-id`` elsewhere - verified via DevTools
        # capture 2026-05-30.
        payload["combinedState"] = {
            "and": [
                {"state": c.state, "value": c.value, "addonid": c.addon_id}
                for c in state.combined_and
            ],
        }
    if state.category is UserStateCategory.ZONE_SENSOR and state.zone_sensor is not None:
        # Verified via DevTools capture 2026-05-30. sensorState.zone holds
        # the threshold config. Hysteresis: active_value vs inactive_value
        # may differ; same value means sharp threshold.
        payload["sensorState"] = {
            "zone": {
                "greaterOnSet": state.zone_sensor.greater_on_set,
                "activeValue": state.zone_sensor.active_value,
                "zoneID": state.zone_sensor.zone_id,
                "groupID": state.zone_sensor.group_id,
                "typeID": state.zone_sensor.type_id,
                "inactiveValue": state.zone_sensor.inactive_value,
            },
        }
    if state.category is UserStateCategory.TRIGGERED:
        # Verified via DevTools capture 2026-05-30. activityState has:
        #   - setTrigger:  flat array of trigger objects (NOT indexed dict)
        #   - resetTrigger: same
        #   - setRetriggeringLag / resetRetriggeringLag: int seconds
        # The "actions" subtree (change-addon-state on self) is auto-generated
        # by the dSS on save - we don't send it.
        payload["activityState"] = {
            "setTrigger": [_serialize_state_trigger(t) for t in state.set_triggers],
            "resetTrigger": [_serialize_state_trigger(t) for t in state.reset_triggers],
            "setRetriggeringLag": state.set_retriggering_lag,
            "resetRetriggeringLag": state.reset_retriggering_lag,
        }

    return payload


def _serialize_state_trigger(trigger: Any) -> dict[str, Any]:
    """Flatten a :class:`ResponderTrigger` into the triggered-state save format.

    Only emits fields that are set on the trigger - matches the captured
    ``{"type": "zone-scene", "zone": 9, "group": 2, "scene": 17}`` shape.
    """
    out: dict[str, Any] = {"type": trigger.trigger_type.value}
    optional_map = {
        "addon-id": trigger.addon_id,
        "name": trigger.name,
        "state": trigger.state,
        "dsuid": trigger.dsuid,
        "msg": trigger.msg,
        "buttonIndex": trigger.button_index,
        "zone": trigger.zone,
        "group": trigger.group,
        "scene": trigger.scene,
        "event": trigger.event,
    }
    for k, v in optional_map.items():
        if v is not None:
            out[k] = v
    return out


def _extract_current_value(raw: Mapping[str, Any]) -> int | str | None:
    """Try a few field names where the dSS stashes the current value."""
    for key in ("value", "current", "state"):
        v = raw.get(key)
        if v is not None:
            return v if isinstance(v, (str, int)) else str(v)
    return None


def _parse_values(raw: Any) -> tuple[UserStateValue, ...]:
    """Optional enum-of-values, e.g. ``{"0":{"value":0,"label":"off"},"1":...}``."""
    if not isinstance(raw, Mapping):
        return ()
    out: list[UserStateValue] = []
    for key in sorted(raw.keys(), key=_int_key):
        v = raw[key]
        if isinstance(v, Mapping):
            try:
                out.append(UserStateValue(
                    value=int(v.get("value", key)),
                    label=str(v.get("label", "")),
                ))
            except (ValueError, TypeError):
                continue
    return tuple(out)


def _int_key(s: str) -> int:
    try:
        return int(s)
    except (ValueError, TypeError):
        return 0
