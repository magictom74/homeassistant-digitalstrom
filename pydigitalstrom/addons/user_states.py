# Parsing
# ---------------------------------------------------------------------------


def parse_user_state(
    state_id: str,
    category: UserStateCategory,
    raw: Mapping[str, Any],
) -> UserState:
    """Convert the raw sub-tree dict into a :class:`UserState`."""
    set_triggers, set_lag = _parse_triggered_set(raw, "setTrigger")
    reset_triggers, reset_lag = _parse_triggered_set(raw, "resetTrigger")
    return UserState(
        state_id=str(raw.get("id", state_id)),
        name=str(raw.get("name", state_id)),
        category=category,
        current_value=_extract_current_value(raw),
        values=_parse_values(raw.get("values")),
        set_name=_str_or_none(raw.get("setName")),
        reset_name=_str_or_none(raw.get("resetName")),
        show_on_phone=_bool_or_none(raw.get("showOnPhone")),
        combined_and=_parse_combined_and(raw),
        set_triggers=set_triggers,
        reset_triggers=reset_triggers,
        set_retriggering_lag=set_lag,
        reset_retriggering_lag=reset_lag,
        zone_sensor=_parse_zone_sensor(raw),
    )


def _parse_zone_sensor(raw: Mapping[str, Any]) -> ZoneSensorStateConfig | None:
    sensor_state = raw.get("sensorState")
    if not isinstance(sensor_state, Mapping):
        return None
    zone = sensor_state.get("zone")
    if not isinstance(zone, Mapping):
        return None
    try:
        return ZoneSensorStateConfig(
            zone_id=int(zone.get("zoneID", 0)),
            group_id=int(zone.get("groupID", 0)),
            type_id=int(zone.get("typeID", 0)),
            active_value=float(zone.get("activeValue", 0)),
            inactive_value=float(zone.get("inactiveValue", 0)),
            greater_on_set=bool(zone.get("greaterOnSet", True)),
        )
    except (ValueError, TypeError):
        return None


def _parse_triggered_set(
    raw: Mapping[str, Any],
    key: str,
) -> tuple[tuple[Any, ...], int]:
    """Parse a setTrigger / resetTrigger sub-object.

    Read-back shape (property tree)::

        "setTrigger": {
            "triggers": {"1": {"type": "zone-scene", ...}},
            "actions": {"0": {...}}
        }

    Save shape (DevTools-captured)::

        "setTrigger": [{"type": "zone-scene", ...}]

    Returns a tuple of (parsed ResponderTriggers, lag-seconds). Both shapes
    are tolerated - we look in ``activityState[key]`` first (save shape),
    then fall back to ``[key]`` (read shape).
    """
    from ..models import ResponderTrigger
    activity = raw.get("activityState")
    container = activity.get(key) if isinstance(activity, Mapping) else None
    if container is None:
        container = raw.get(key)

    triggers_out: list[Any] = []
    if isinstance(container, list):
        for item in container:
            if isinstance(item, Mapping):
                triggers_out.append(ResponderTrigger.from_raw(item))
    elif isinstance(container, Mapping):
        # Read shape: {"triggers": {"1": {...}}} or flat indexed dict
        inner = container.get("triggers")
        if isinstance(inner, Mapping):
            for k in sorted(inner.keys(), key=_int_key_safe):
                v = inner[k]
                if isinstance(v, Mapping):
                    triggers_out.append(ResponderTrigger.from_raw(v))
        else:
            for k in sorted(container.keys(), key=_int_key_safe):
                v = container[k]
                if isinstance(v, Mapping) and "type" in v:
                    triggers_out.append(ResponderTrigger.from_raw(v))

    # Retriggering lag - look in activityState (save shape)
    lag = 0
    if isinstance(activity, Mapping):
        lag_key = "setRetriggeringLag" if key == "setTrigger" else "resetRetriggeringLag"
        try:
            lag = int(activity.get(lag_key, 0))
        except (ValueError, TypeError):
            lag = 0
    return tuple(triggers_out), lag


def _int_key_safe(s: Any) -> int:
    try:
        return int(s)
    except (ValueError, TypeError):
        return 0


def _parse_combined_and(raw: Mapping[str, Any]) -> tuple[CombinedCondition, ...]:
    """Extract combined-state AND conditions.

    Looks at ``combinedState.and`` (save payload shape) first, then
    ``combined.and`` (legacy / property-tree-read shape). Each condition
    has ``state``, ``value`` plus either ``addonid`` (combined-states
    save) or ``addon-id`` (legacy/read shape).
    """
    for container_key in ("combinedState", "combined"):
        container = raw.get(container_key)
        if not isinstance(container, Mapping):
            continue
        and_list = container.get("and")
        if not isinstance(and_list, list):
            continue
        out: list[CombinedCondition] = []
        for item in and_list:
            if not isinstance(item, Mapping):
                continue
            addon_id = item.get("addonid") or item.get("addon-id") or ""
            out.append(CombinedCondition(
                state=str(item.get("state", "")),
                value=str(item.get("value", "")),
                addon_id=str(addon_id),
            ))
        return tuple(out)
    return ()


def _str_or_none(v: Any) -> str | None:
    if v is None or v == "":
        return None
    return str(v)


def _bool_or_none(v: Any) -> bool | None:
    if v is None:
        return None
    if isinstance(v, bool):
        return v
    if isinstance(v, (int, float)):
        return bool(v)
    if isinstance(v, str):
        s = v.strip().lower()
        if s in ("true", "1", "yes"):
            return True
        if s in ("false", "0", "no"):
            return False
    return None


