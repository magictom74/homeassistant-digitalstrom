"""``system-addon-user-defined-states`` wrapper.

Unlike timed-events and scene-responders, this addon's tree is **not** a
flat ``entries/`` list - it has four sub-categories under
``/scripts/system-addon-user-defined-states/``:

- ``device-sensor-states`` - states bound to physical sensor values
- ``custom-states`` - user-toggleable switches (set/reset labels)
- ``combined-states`` - logical combinations (AND/OR) of other states
- ``triggered-states`` - event-driven states with set+reset triggers

Save protocol verified via DevTools capture 2026-05-30:

- Event-Name: ``system-addon-user-defined-states.config`` (DOT)
- Reply-Event: ``system-addon-user-defined-states.saved`` (DOT)
- **Universal payload** with a ``type`` discriminator field (matches the
  category). All other category-specific containers must be present but
  may be empty/null defaults.
"""

from __future__ import annotations

import logging
from typing import Any, Mapping

from ..exceptions import DssNotFoundError, DssProtocolError
from ..models import (
    CombinedCondition,
    UserState,
    UserStateCategory,
    UserStateValue,
    ZoneSensorStateConfig,
)
from .base import AddonBase

_LOGGER = logging.getLogger(__name__)


class UserStatesAddon(AddonBase):
    """Wrapper around ``/scripts/system-addon-user-defined-states/<category>/<id>``."""

    addon_name = "system-addon-user-defined-states"

    CATEGORIES: tuple[UserStateCategory, ...] = (
        UserStateCategory.DEVICE_SENSOR,
        UserStateCategory.CUSTOM,
        UserStateCategory.COMBINED,
        UserStateCategory.TRIGGERED,
    )

    async def list_states(
        self,
        category: UserStateCategory | None = None,
    ) -> list[UserState]:
        """List all states, optionally filtered by category."""
        categories = (category,) if category is not None else self.CATEGORIES
        out: list[UserState] = []
        for cat in categories:
            ids = await self._list_ids_in_category(cat)
            for state_id in ids:
                try:
                    state = await self._load_state(cat, state_id)
                except Exception as exc:  # noqa: BLE001
                    _LOGGER.debug(
                        "[pydss.addons.user_states] %s/%s load failed: %s",
                        cat.value, state_id, exc,
                    )
                    continue
                if state is not None:
                    out.append(state)
        return out

    async def get_state(self, state_id: str, category: UserStateCategory | None = None) -> UserState | None:
        """Look up one state by id; if category unknown, scan all."""
        categories = (category,) if category is not None else self.CATEGORIES
        for cat in categories:
            state = await self._load_state(cat, state_id)
            if state is not None:
                return state
        return None

    async def set_state_value(self, state_name: str, value: str | int) -> None:
        """Set a user-state's current value.

        Uses the global ``/json/state/set`` endpoint - the state name is the
        addressable identifier in the state namespace (not the numeric
        state-id under ``user-defined-states/``). Look up the name via
        :meth:`get_state` first.
        """
        await self._client.get(
            "/json/state/set",
            params={"name": state_name, "value": str(value)},
        )
        _LOGGER.info("[pydss.addons.user_states] set state %s = %s", state_name, value)

    async def get_state_value(self, state_name: str) -> str | None:
        """Read a user-state's current value via ``/json/state/get``."""
        result = await self._client.get(
            "/json/state/get",
            params={"name": state_name},
        )
        if isinstance(result, Mapping):
            value = result.get("value")
            if value is not None:
                return str(value)
        return None

    async def save_state(
        self,
        state: UserState,
        *,
        wait_for_reply: bool = True,
        reply_timeout_s: float = 5.0,
    ) -> str | None:
        """Create or update a user-defined state.

        On create, set ``state.state_id`` to an empty string - the dSS
        assigns a Unix-timestamp-style id automatically.

        Supported categories (verified via DevTools capture 2026-05-30):

        - :attr:`UserStateCategory.CUSTOM` - fully supported (CRUD)
        - :attr:`UserStateCategory.COMBINED` - structure ready, save
          format extrapolated from custom-states pattern (not yet
          live-verified - try and report)
        - :attr:`UserStateCategory.TRIGGERED` - structure ready, save
          format extrapolated (not yet live-verified)
        - :attr:`UserStateCategory.DEVICE_SENSOR` - read-only typically,
          generated by other addons

        Returns:
            The state id on success, or None on reply timeout.
        """
        payload = serialize_user_state_for_save(state)
        reply = await self.raise_config(
            action="save",
            value=payload,
            wait_for_reply=wait_for_reply,
            reply_timeout_s=reply_timeout_s,
        )
        if not wait_for_reply:
            return None
        if reply is None:
            _LOGGER.warning("[pydss.addons.user_states] save: no reply within timeout")
            return None
        if reply.get("complete") != "success":
            _LOGGER.warning("[pydss.addons.user_states] save reply: %s", dict(reply))

        # The saved-event payload includes the (newly assigned) id directly -
        # no list-scan needed. Falls back to name-lookup if the reply is
        # missing the id for some reason.
        if "id" in reply:
            return str(reply["id"])
        if not state.state_id:
            after = await self.list_states(state.category)
            for s in after:
                if s.name == state.name:
                    return s.state_id
            return None
        return state.state_id

    async def delete_state(
        self,
        state_id: str,
        category: UserStateCategory | None = None,
    ) -> None:
        """Delete a user-defined state by id via property-tree removal.

        If ``category`` is None, scans all categories to find the state.
        Pass the category explicitly when known to save one round-trip.
        """
        if category is None:
            state = await self.get_state(state_id)
            if state is None:
                raise DssNotFoundError(
                    f"[pydss.addons.user_states] no state {state_id!r}"
                )
            category = state.category

        path = f"/scripts/system-addon-user-defined-states/{category.value}/{state_id}"
        try:
            await self._client.get("/json/property/remove", params={"path": path})
            _LOGGER.info("[pydss.addons.user_states] removed %s", path)
        except DssProtocolError as exc:
            msg = str(exc).lower()
            if "could not find" in msg or "not found" in msg:
                raise DssNotFoundError(
                    f"[pydss.addons.user_states] no state {state_id!r} in {category.value}"
                ) from exc
            raise

    async def _list_ids_in_category(self, category: UserStateCategory) -> list[str]:
        path = f"{self.base_path}/{category.value}"
        try:
            children = await self._walker.get_children(path)
        except Exception as exc:  # noqa: BLE001
            _LOGGER.debug("[pydss.addons.user_states] %s missing: %s", path, exc)
            return []
        return [c.name for c in children if c.is_leaf is False]

    async def _load_state(
        self,
        category: UserStateCategory,
        state_id: str,
    ) -> UserState | None:
        path = f"{self.base_path}/{category.value}/{state_id}"
        try:
            raw = await self._walker.walk(path)
        except Exception:  # noqa: BLE001
            return None
        if not raw:
            return None
        return parse_user_state(state_id, category, raw)


