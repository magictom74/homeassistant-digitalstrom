def _to_int_safe(v: Any) -> int | None:
    if v is None:
        return None
    try:
        return int(v)
    except (ValueError, TypeError):
        return None


def _to_float_safe(v: Any) -> float | None:
    if v is None:
        return None
    try:
        return float(v)
    except (ValueError, TypeError):
        return None


def _to_bool_safe(v: Any) -> bool | None:
    if v is None:
        return None
    if isinstance(v, bool):
        return v
    if isinstance(v, (int, float)):
        return bool(v)
    if isinstance(v, str):
        return v.strip().lower() in ("true", "1", "yes")
    return None


def _parse_dss_timestamp(v: Any) -> datetime | None:
    """Parse the dSS server-local timestamp format ``YYYY-MM-DD HH:MM:SS``.

    Returns a naive datetime (no tzinfo) to match the dSS server's local
    clock. Library users comparing against ``datetime.now()`` (also naive
    local) will get sensible age values as long as the dSS and the host
    run in the same timezone - which is the common case.

    Returns None for missing values or epoch markers (``1970-01-01...``).
    """
    if v is None:
        return None
    s = str(v).strip()
    if not s or s.startswith("1970-01-01"):
        return None
    for fmt in ("%Y-%m-%d %H:%M:%S", "%Y-%m-%dT%H:%M:%S"):
        try:
            return datetime.strptime(s, fmt)
        except ValueError:
            continue
    return None
